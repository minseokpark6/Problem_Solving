def solution(n):
    # 피보나치 수열 1번째, 2번째 항 변수 지정
    a, b = 1, 1
    if n == 1:
        answer = 1
    else:
        for i in range(1, n):
            a, b = b, a+b
        answer = b
    return answer % 1234567



